"""
C64 Memory Map and I/O Constants
"""

# C64 Memory Map Constants
ROM_BASIC_START = 0xA000
ROM_BASIC_END = 0xC000
ROM_KERNAL_START = 0xE000
ROM_KERNAL_END = 0x10000
ROM_CHAR_START = 0xD000
ROM_CHAR_END = 0xE000

RAM_START = 0x0000
RAM_END = 0x10000

# I/O Addresses
VIC_BASE = 0xD000
SID_BASE = 0xD400
CIA1_BASE = 0xDC00
CIA2_BASE = 0xDD00

# IRQ vector
IRQ_VECTOR = 0x0314

# Screen memory (default)
SCREEN_MEM = 0x0400
COLOR_MEM = 0xD800
